<?php
//operadores de comparação

$a = 55.0;
$b = 55;

var_dump($a > $b); //maior que

echo "<br/>";

var_dump($a < $b); //menor que

echo "<br/>";

var_dump($a == $b); //comparar igualdade

echo "<br/>";

var_dump($a === $b); //compara indentidade

echo "<br/>";

var_dump($a != $b); //não é igual?

echo "<br/>";

var_dump($a !== $b); //não é identico?

?>